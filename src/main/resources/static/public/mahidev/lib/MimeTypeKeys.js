/**
 * retourne la clé depuis la valeur
 * @param {String} value, la valeur du MimeType
 * @returns {String} la clé correspondante si present, sinon return OTHER
 */
export const getMimeTypeKey = value => {
    let key = Object.keys(MimeTypeKeys).find(key => MimeTypeKeys[key] === value);
    return _isNotNullOrUndefined(key) ? key : getMimeTypeKey(MimeTypeKeys.OTHER);
}